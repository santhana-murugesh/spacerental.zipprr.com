export const RTL_LANGUAGES = ['ar', 'he', 'fa', 'ur'];
i18n
  .use(HttpApi) // Load translations using http backend
  .use(LanguageDetector) // Detect user language
  .use(initReactI18next) // Pass the i18n instance to react-i18next
  .init({
    resources,
    fallbackLng: 'en',
    debug: process.env.NODE_ENV === 'development',
    interpolation: {
      escapeValue: false, // React already does escaping
    },
    detection: {
      // Order of language detection
      order: ['localStorage', 'sessionStorage', 'navigator', 'htmlTag'],
      // Keys to lookup language from
      lookupLocalStorage: 'selectedLanguage',
      lookupSessionStorage: 'selectedLanguage',
      // Cache user language
      caches: ['localStorage', 'sessionStorage'],
    },
    backend: {
      // Path where resources are loaded from (only as fallback)
      loadPath: '/api/translations/{{lng}}',
      // Allow cross domain requests
      crossDomain: false,
      // Parse data after it has been fetched
      parse: function(data) {
        try {
        const parsed = JSON.parse(data);
        return parsed.success ? parsed.data : {};
        } catch (e) {
          console.warn('Failed to parse backend translations, using hardcoded ones');
          return {};
        }
      },
      // Always load from backend for non-English languages, use hardcoded for English as fallback
      request: function(options, url, payload, callback) {
        const currentLang = i18n.language || 'en';
        // For English, use hardcoded translations as primary source
        if (currentLang === 'en' && resources[currentLang] && resources[currentLang].translation) {
          callback(null, { status: 200, data: '{}' });
          return;
        }
        // For all other languages, always fetch from backend
        fetch(url)
          .then(response => response.text())
          .then(data => {
            callback(null, { status: 200, data });
          })
          .catch(error => {
            console.error('Translation API error for', currentLang, ':', error);
            callback(error, { status: 500, data: '{}' });
          });
      }
    }
  });
export const changeLanguage = (languageCode, direction = 'ltr') => {
  return new Promise((resolve) => {
    if (languageCode !== 'en') {
      i18n.reloadResources([languageCode], ['translation']).then(() => {
        proceedWithLanguageChange();
      }).catch(error => {
        console.error('Failed to reload translation resources:', error);
        proceedWithLanguageChange();
      });
    } else {
      proceedWithLanguageChange();
    }
    function proceedWithLanguageChange() {
      i18n.changeLanguage(languageCode, () => {
        const isRTL = RTL_LANGUAGES.includes(languageCode) || direction === 'rtl';
        document.documentElement.dir = isRTL ? 'rtl' : 'ltr';
        document.documentElement.lang = languageCode;
        if (isRTL) {
          document.body.style.direction = 'rtl';
          document.body.style.textAlign = 'right';
        } else {
          document.body.style.direction = 'ltr';
          document.body.style.textAlign = 'left';
        }
        localStorage.setItem('selectedLanguage', JSON.stringify({
          code: languageCode,
          direction: isRTL ? 'rtl' : 'ltr'
        }));
        resolve(languageCode);
      });
    }
  });
};
export const getCurrentDirection = () => {
  const currentLang = i18n.language;
  return RTL_LANGUAGES.includes(currentLang) ? 'rtl' : 'ltr';
};
export const isRTL = () => {
  return getCurrentDirection() === 'rtl';
};
export default i18n;
